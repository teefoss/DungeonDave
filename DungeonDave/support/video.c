//
// 	LoadTextureFromFile
/// Returns a texture_t with texture initialized and size loaded
//
texture_t V_LoadTexture (const char *file)
{
	SDL_Surface *s;
	texture_t t;
	
	s = IMG_Load(file);
	if (!s) {
		printf("LoadTexture: Could not load '%s'!\n", file);
		V_ShutDown();
		exit(1);
	}
	
	t.sdltx = SDL_CreateTextureFromSurface(renderer, s);
	t.w = s->w;
	t.h = s->h;
	
	SDL_FreeSurface(s);
	return t;
}

void V_RenderTexture (texture_t *tx, int x, int y)
{
	SDL_Rect r = { x, y, tx->w, tx->h };
	SDL_RenderCopy(renderer, tx->sdltx, NULL, &r);
}


// render graphic g at x, y (no color)
// graphic should use 0 for transparent, 1 for pixel
void V_RenderGraphic (graphic_t *g, int x, int y)
{
	int destx,desty;
	char *data;
	
	data = g->data;
	
	for ( desty = 0; desty < g->h*1; desty += 1 )
		for ( destx=0; destx < g->w*1 ; destx += 1, data++ )
		{
			if (*data) {
				SDL_RenderDrawPoint(renderer, x+destx, y+desty);
				SDL_RenderDrawPoint(renderer, x+destx+0, y+desty);
				SDL_RenderDrawPoint(renderer, x+destx, y+desty+0);
				SDL_RenderDrawPoint(renderer, x+destx+0, y+desty+0);
			}
		}
}


// render graphic g at x, y from color data
// graphic data should store pixel location with color value
void V_RenderGraphicC (graphic_t *g, int x, int y)
{
	int destx,desty;
	char *data;
	
	data = g->data;
	
	for (desty=0 ; desty<g->h ; desty++)
	for (destx=0 ; destx<g->w ; destx++,data++)
	{
		if (*data != 99)
		{
			V_SetColor(*data);
			SDL_RenderDrawPoint(renderer, x+destx, y+desty);
		}
	}
}




void
V_InitVideo
( int screenw,
  int screenh,
  int scale,
  const char *winname)
{
	uint32_t 	flags;
	float 		hdpi, vdpi;

	if ( SDL_Init( SDL_INIT_VIDEO ) < 0 ) {
		puts("SDL_Init failed");
		exit(1);
	}

	
	// WINDOW
	
	flags = SDL_WINDOW_SHOWN | SDL_WINDOW_ALLOW_HIGHDPI;
	window = SDL_CreateWindow( winname,0,0,screenw*scale,screenh*scale,flags );
	if ( !window )
	{
		puts("SDL_CreateWindow failed");
		exit(1);
	}

	// https://discourse.libsdl.org/t/macos-10-14-mojave-issues/25060
	// needed for OpenGL bug in OS X Mojave
	SDL_GL_CreateContext(window);
	SDL_PumpEvents();
	SDL_SetWindowSize(window, screenw*scale, screenh*scale);

	
	// RENDERER
	
	flags = SDL_RENDERER_ACCELERATED | SDL_RENDERER_PRESENTVSYNC;
	renderer = SDL_CreateRenderer(window, -1, flags);
	if (!renderer)
	{
		puts("SDL_CreateRenderer failed");
		exit(1);
	}
	SDL_GetDisplayDPI(0, NULL, &hdpi, &vdpi);
	if (hdpi > 100 || vdpi > 100) // for apple retina weirdness
		SDL_RenderSetScale(renderer, scale*2, scale*2);
	else
		SDL_RenderSetScale(renderer, scale, scale);
	SDL_SetRenderDrawBlendMode(renderer, SDL_BLENDMODE_BLEND);
	
	vinfo.width = screenw;
	vinfo.height = screenh;
	vinfo.rows = screenh/8;
	vinfo.cols = screenw/8;
	vinfo.color = 7;
	vinfo.cursorx = vinfo.cursory = 0;
	
	textbuffer = (textcell_t *)malloc(sizeof(textcell_t)*vinfo.rows*vinfo.cols);
	for (int i=0 ; i<vinfo.rows*vinfo.cols ; i++)
		memset(&textbuffer[i], 0, sizeof(textcell_t));
	puts("Video Initialized.");
}


void V_ShutDown (void)
{
	SDL_DestroyRenderer(renderer);
	SDL_DestroyWindow(window);
	free(textbuffer);
	SDL_Quit();
}


static int starttic;
static int prevtic;
//static int endtics;
float dt;

// call at the start of any loop that needs fr limit
void V_StartFrame (void)
{
	starttic = SDL_GetTicks();
	prevtic = starttic;
}

// call at the end of any loop that needs fr limit
void V_LimitFR (unsigned fps)
{
	float limit;
	
	limit = 1000.0f / (float)fps;
	dt = SDL_GetTicks() - starttic;
//	printf("dt = %f ms\n",dt);
	
	if (dt < limit)
		SDL_Delay(limit-dt);
	dt /= 1000.0f;
}


