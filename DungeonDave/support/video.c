void textcolor (int c)
{
	vinfo.color = c;
}

void gotoxy(unsigned x, unsigned y)
{
	if (x > vinfo.cols || y > vinfo.rows)
	{
		puts("gotoxy: invalid coordinate");
		return;
	}
	vinfo.cursorx = x;
	vinfo.cursory = y;
}



void print (char *string)
{
	textcell_t *cell;
	char *s;
	
	cell = &textbuffer[vinfo.cols*vinfo.cursory+vinfo.cursorx];
	s = string;
	
	while (*s != '\0')
	{
		if (!cell) // off the end
			return;
		cell->chr = *s;
		cell->color = vinfo.color;
		s++; cell++;
	}	
}


void printxy (unsigned x, unsigned y, char *string)
{
	gotoxy(x, y);
	print(string);
}


void printintxy (unsigned x, unsigned y, int i)
{
	char buf[80];
	
	sprintf(buf, "%i", i);
	printxy(x, y, buf);
}


