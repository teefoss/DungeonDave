// set the renderer's draw color
void V_SetColor (int c)
{
	if (c < BLACK || c > BRIGHTWHITE) {
		puts("V_SetColor: invalid color");
		return;
	}
	SDL_SetRenderDrawColor(renderer, colors[c].r, colors[c].g, colors[c].b, 255);
}


void V_Clear (int color)
{
	V_SetColor(color);
	SDL_RenderClear(renderer);
}


//
//	T_RenderCell
//  render one text cell
//	called by T_RenderTextBuffer for each cell in textbuffer
//
void RenderCell (textcell_t *cell, int row, int col)
{
	int x,y;
	
	for (y=0 ; y<8 ; y++)
		for (x=0 ; x<8 ; x++)
			if (font8x8_basic[cell->chr][y] >> x & 1)
			{
				V_SetColor(cell->color % 16);
				SDL_RenderDrawPoint(renderer,col*8+x,row*8+y);
			}
}


void V_RenderTextBuffer (void)
{
	textcell_t	*cell;
	int 		row,col;
	int 		blinkflag;
	
	blinkflag = (SDL_GetTicks() % 550 < 275) ? 1 : 0;
	
	for (row=0,cell=&textbuffer[0]; row<vinfo.rows; row++)
		for (col=0; col<vinfo.cols; col++, cell++)
		{
			if (cell->color <= BRIGHTWHITE)
				RenderCell(cell, row, col);
			else if (cell->color > BRIGHTWHITE && blinkflag)
				RenderCell(cell, row, col);
		}
}



void V_Draw (void)
{
	V_RenderTextBuffer();
	SDL_RenderPresent(renderer);
}


